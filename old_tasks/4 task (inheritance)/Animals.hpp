#pragma once

#include <string>
#include <iostream>

/**
 * @brief Абстрактный базовый класс для всех животных.
 *
 * Этот класс задаёт общий интерфейс и базовое состояние для всех конкретных
 * животных. Он инкапсулирует общее поведение, которое может быть
 * полиморфически вызвано у любого наследника.
 *
 * Класс является абстрактным: создание экземпляров напрямую запрещено.
 * Все производные классы обязаны реализовать чисто виртуальные методы.
 *
 * @note Использует отношение "is-a": каждый наследник — это Animal.
 */
class Animal {
protected:
    /**
     * @brief Имя животного.
     *
     * Устанавливается один раз при создании и не изменяется в течение жизни объекта.
     * Доступно для чтения через метод getName(), а также для использования
     * в производных классах (например, при выводе сообщений).
     */
    std::string name;

public:
    /**
     * @brief Конструктор базового класса.
     *
     * @param name Имя животного. Не должно быть пустым (рекомендуется, но не проверяется).
     */
    Animal(const std::string& name);

    /**
     * @brief Виртуальный деструктор.
     *
     * Обеспечивает корректное удаление объектов через указатель на Animal.
     * Без него деструкторы производных классов не будут вызваны.
     */
    virtual ~Animal() = default;

    /**
     * @brief Возвращает имя животного.
     *
     * @return const std::string& — ссылка на внутреннее имя (для эффективности).
     */
    std::string getName() const;

    /**
     * @brief Издаёт характерный звук.
     *
     * Должен быть переопределён в каждом производном классе.
     * Например: кошка — "Meow!", собака — "Woof!".
     */
    virtual void speak() const = 0;

    /**
     * @brief Описывает способ передвижения животного.
     *
     * Должен быть специфичен для каждого вида (например, "тихо ходит", "энергично бегает").
     */
    virtual void move() const = 0;

    /**
     * @brief Возвращает ожидаемую продолжительность жизни в годах.
     *
     * Зависит от вида, породы, особенностей. Используется для демонстрации
     * полиморфного расчёта.
     */
    virtual int getLifespan() const = 0;

    /**
     * @brief Выражает радость или удовлетворение.
     *
     * Виртуальный метод с реализацией по умолчанию ("seems content").
     * Может быть переопределён в производных классах для специфичного поведения:
     * например, кошка мурлычет, собака виляет хвостом.
     *
     * @note Не является чисто виртуальным — наследники могут не переопределять его.
     */
    virtual void expressHappiness() const;
};

/**
 * @brief Класс, представляющий кошку — конкретное животное.
 *
 * Кошка характеризуется именем и количеством жизней (традиция: 9 жизней).
 * Реализует все обязательные методы Animal и добавляет уникальное поведение — мурлыканье.
 *
 * @note Количество жизней влияет на вычисление продолжительности жизни (шутливая логика).
 */
class Cat : public Animal {
    /**
     * @brief Количество жизней кошки.
     *
     * Традиционно 9, но может быть задано иначе. Минимальное значение — 1.
     * Используется в методе getLifespan().
     */
    int lives;

public:
    /**
     * @brief Конструктор кошки.
     *
     * @param n Имя кошки.
     * @param l Количество жизней (по умолчанию — 9).
     *          Если передано значение <= 0, устанавливается 1.
     */
    Cat(const std::string& n, int l = 9);

    /// @copydoc Animal::speak()
    void speak() const override;

    /// @copydoc Animal::move()
    void move() const override;

    /// @copydoc Animal::getLifespan()
    int getLifespan() const override;

    /**
     * @brief Мурлычет — уникальное поведение кошки.
     *
     * Этот метод отсутствует в базовом классе и доступен только при работе
     * с объектом типа Cat. Для вызова через Animal* требуется dynamic_cast.
     */
    void purr() const;

    /// @copydoc Animal::expressHappiness()
    void expressHappiness() const override;
};

/**
 * @brief Класс, представляющий собаку — конкретное животное.
 *
 * Собака характеризуется именем и породой. Порода влияет на расчёт
 * продолжительности жизни (например, крупные породы живут меньше).
 *
 * Добавляет уникальное поведение — принесение предмета (fetch).
 */
class Dog : public Animal {
    /**
     * @brief Порода собаки.
     *
     * Строка, задающая тип собаки (например, "Golden Retriever", "Great Dane").
     * Используется в speak() и getLifespan().
     */
    std::string breed;

public:
    /**
     * @brief Конструктор собаки.
     *
     * @param n Имя собаки.
     * @param b Порода собаки (не может быть пустой в логике приложения,
     *          но технически допускается).
     */
    Dog(const std::string& n, const std::string& b);

    /// @copydoc Animal::speak()
    void speak() const override;

    /// @copydoc Animal::move()
    void move() const override;

    /// @copydoc Animal::getLifespan()
    int getLifespan() const override;

    /**
     * @brief Принести предмет (например, мяч).
     *
     * Уникальное поведение собаки. Не определено в Animal.
     * Для вызова через Animal* требуется dynamic_cast.
     */
    void fetch() const;

    /// @copydoc Animal::expressHappiness()
    void expressHappiness() const override;
};